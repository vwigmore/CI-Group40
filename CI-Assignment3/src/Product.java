


public class Product {
	
	private Node node;
	private int number;

	public Product(int number, Node node) {
		this.node = node;
		this.number = number;
	}

	public Node getNode() {
		return node;
	}

	public void setNode(Node node) {
		this.node = node;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

}
